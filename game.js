    // ステージを描画する関数
    draw() {

        /* 枠線の色と太さ */
        stroke(0, 0, 0);
        strokeWeight(1);

        /* ステージの描画 */
        for(let i=0; i<this.start_stage.length; i++) {
            for(let j=0; j<this.start_stage[0].length; j++) {
                if(this.start_stage[i][j] === 0){
                    fill(0, 0, 0);
                }
                else if(this.start_stage[i][j] === 1) {  // 配列の中身が1の時
                    fill(51, 51, 153);
                }
                else if(this.start_stage[i][j] === 2){
                    // imageMode(CENTER);
                    // scale(0.02, 0.02);
                    // image(pacManImg, 500*(j+1), 500*(i+1));
                    fill(255, 255, 0);
                    this.turn++;
                }
                else if(this.start_stage[i][j] === 3){
                    fill(255, 0, 0);
                }
                else if(this.start_stage[i][j] === 4){
                    fill(0, 255, 0);
                }
                else if(this.start_stage[i][j] === 5){
                    fill(0, 255, 255);
                    this.baitFlag=1;
                }

                rect(h*(j+1), w*(i+1), h, w)    // ブロックの描画
            }
        }

        if (this.baitFlag === 1) {
            this.baitFlag = 0;
        } else if (this.baitFlag === 0) {
            this.isGameClear = 1;
        }
    }

    // ゲームターンを出力する関数
